"""
Logical operators are used to manipulate the logic of Boolean expressions.

and  AND
or   OR
not  NOT
"""
# Logical Expressions

# OR Expression
my_bool = True or False
print(my_bool)

#AND Expression
my_bool = True and False
print(my_bool)

# NOT Expresion
my_bool = False
print(not my_bool)

# Bit Value
# In bit terms, the value of True es 1 and False is 0.

print(10 * True) # 10 * 1
print(10 * False) # 10 * 0
